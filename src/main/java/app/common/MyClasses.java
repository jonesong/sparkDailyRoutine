package app.common;

public class MyClasses {

	String str1, str2, str3, str4, str5;
	
	public MyClasses(String str1) {
		this.str1 = str1;
	}

	public MyClasses(String str1, String str2) {
		this.str1 = str1;
		this.str2 = str2;
	}

	public MyClasses(String str1, String str2, String str3) {
		this.str1 = str1;
		this.str2 = str2;
		this.str3 = str3;
	}

	public MyClasses(String str1, String str2, String str3, String str4) {
		this.str1 = str1;
		this.str2 = str2;
		this.str3 = str3;
		this.str4 = str4;
	}

	public MyClasses(String str1, String str2, String str3, String str4, String str5) {
		this.str1 = str1;
		this.str2 = str2;
		this.str3 = str3;
		this.str4 = str4;
		this.str5 = str5;
	}

}
